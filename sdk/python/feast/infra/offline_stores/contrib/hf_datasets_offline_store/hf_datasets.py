class HFDatasetsRetrievalJob(RetrievalJob):
    """
    RetrievalJob implementation for Hugging Face datasets.

    This class encapsulates:
        - lazy evaluation
        - export utilities
        - Hub publishing
        - dataset artifact generation
        - metadata lineage
    """

    def __init__(
        self,
        evaluation_function,
        full_feature_names: bool = False,
        metadata: Optional[Dict[str, Any]] = None,
    ):
        self._evaluation_function = evaluation_function
        self._full_feature_names = full_feature_names
        self._metadata = metadata or {}

        self._arrow_table: Optional[pa.Table] = None

    # -------------------------------------------------------------------------
    # Internal Evaluation
    # -------------------------------------------------------------------------

    def _evaluate_arrow(self) -> pa.Table:
        """
        Lazily evaluate retrieval into Arrow format.
        """

        if self._arrow_table is None:
            self._arrow_table = self._evaluation_function()

        return self._arrow_table

    # -------------------------------------------------------------------------
    # Feast RetrievalJob API
    # -------------------------------------------------------------------------

    def to_arrow(self) -> pa.Table:
        return self._evaluate_arrow()

    def to_df(self) -> pd.DataFrame:
        return self.to_arrow().to_pandas()

    # -------------------------------------------------------------------------
    # HF-native Exports
    # -------------------------------------------------------------------------

    def to_hf_dataset(self) -> Dataset:
        """
        Export directly to Hugging Face Dataset.
        """

        return Dataset(self.to_arrow())

    def save_to_disk(self, path: str):
        """
        Save dataset artifact locally.
        """

        dataset = self.to_hf_dataset()

        dataset.save_to_disk(path)

    def to_parquet(self, path: str):
        """
        Export retrieval result to parquet.
        """

        self.to_hf_dataset().to_parquet(path)

    # -------------------------------------------------------------------------
    # Dataset Card Generation
    # -------------------------------------------------------------------------

    def generate_dataset_card(
        self,
        title: Optional[str] = None,
        description: Optional[str] = None,
        license: str = "apache-2.0",
        tags: Optional[List[str]] = None,
    ) -> str:
        """
        Generate Hugging Face dataset card markdown.
        """

        dataset = self.to_hf_dataset()

        schema_lines = []

        for column_name, feature in dataset.features.items():
            schema_lines.append(
                f"| {column_name} | {feature} |"
            )

        schema_markdown = "\n".join(schema_lines)

        feature_refs = self._metadata.get("feature_refs", [])
        retrieval_timestamp = datetime.utcnow().isoformat()

        card = f"""
---
license: {license}
tags:
{os.linesep.join([f'  - {tag}' for tag in (tags or ['feast', 'feature-store', 'datasets'])])}
---

# {title or "Feast Feature Retrieval Dataset"}

{description or "Generated using Feast historical feature retrieval."}

## Retrieval Metadata

- Retrieval timestamp: {retrieval_timestamp}
- Number of feature refs: {len(feature_refs)}

## Feature References

{os.linesep.join([f"- {ref}" for ref in feature_refs])}

## Dataset Schema

| Column | Type |
|--------|------|
{schema_markdown}

## Generated By

- Feast
- Hugging Face Datasets
- HFDatasetsOfflineStore
"""

        return card.strip()

    # -------------------------------------------------------------------------
    # Hub Publishing
    # -------------------------------------------------------------------------

    def push_to_hub(
        self,
        repo_id: str,
        split: str = "train",
        private: bool = False,
        token: Optional[str] = None,
        generate_card: bool = True,
        card_title: Optional[str] = None,
        card_description: Optional[str] = None,
        card_license: str = "apache-2.0",
        card_tags: Optional[List[str]] = None,
        exist_ok: bool = True,
        **kwargs,
    ):
        """
        Push retrieval result to Hugging Face Hub.

        Features:
            - automatic repo creation
            - dataset card generation
            - lineage metadata
            - split support
        """

        dataset = self.to_hf_dataset()

        api = HfApi(token=token)

        # ---------------------------------------------------------------------
        # Create Repository
        # ---------------------------------------------------------------------

        api.create_repo(
            repo_id=repo_id,
            repo_type="dataset",
            private=private,
            exist_ok=exist_ok,
        )

        # ---------------------------------------------------------------------
        # Generate Dataset Card
        # ---------------------------------------------------------------------

        readme_content = None

        if generate_card:
            readme_content = self.generate_dataset_card(
                title=card_title,
                description=card_description,
                license=card_license,
                tags=card_tags,
            )

        # ---------------------------------------------------------------------
        # Temporary Upload Directory
        # ---------------------------------------------------------------------

        with tempfile.TemporaryDirectory() as tmpdir:

            dataset_path = os.path.join(tmpdir, "dataset")

            dataset.save_to_disk(dataset_path)

            # Write README.md
            if readme_content is not None:
                readme_path = os.path.join(tmpdir, "README.md")

                with open(readme_path, "w", encoding="utf-8") as f:
                    f.write(readme_content)

            # Upload Dataset Directory
            api.upload_folder(
                folder_path=tmpdir,
                repo_id=repo_id,
                repo_type="dataset",
            )

    # -------------------------------------------------------------------------
    # Metadata
    # -------------------------------------------------------------------------

    @property
    def metadata(self) -> Dict[str, Any]:
        return self._metadata


